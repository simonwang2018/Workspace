'''变量的概念基本上和初中代数的方程变量是一致的，只是在计算机程序中，变量不仅可以是数字，还可以是任意数据类型。
变量在程序中就是用一个变量名表示了，变量名必须是大小写英文、数字和_的组合，且不能用数字开头'''
a = 1
#变量a是一个整数。

t_007 = 'T007'
#变量t_007是一个字符串。

Answer = True
#变量Answer是一个布尔值True。

#在Python中，等号=是赋值语句，可以把任意数据类型赋值给变量，同一个变量可以反复赋值，而且可以是不同类型的变量，例如：
a = 123 #a是整数
print(a)
a = 'ABC'  #a变为字符串
print(a)
