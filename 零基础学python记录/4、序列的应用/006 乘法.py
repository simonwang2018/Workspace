# Description : 使用数字n乘以一个序列会生成新的序列。新序列的内容为原来序列被重复n次的结果。如下，将一个序列乘以3生成一个新的序列。
phone = ["华为Mate 10","小米40"]
print(phone * 3)

# 在进行序列的乘法运算时，还可以实现初始化指定长度列表的功能。例如下面的代码，将创建一个长度为5的列表，列表的每个元素都是none,表示没有。
emptylist = [None] * 5
print(emptylist)

